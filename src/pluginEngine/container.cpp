#include "container.h"

#include "toolbardata.h"

void Container::createToolbar(std::string const & name, wxFrame* parent)
{
	//create & register container
	first=new wxAuiToolBar(parent);
	second=wxAuiPaneInfo().Name(name).ToolbarPane().Caption(name).Layer(10).Top().Gripper();
}

void Container::createItem(ToolbarData &toolbar, long id)
{
	toolbar.addTo(first,id);
}
